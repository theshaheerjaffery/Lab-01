#include <iostream>
using namespace std;
int main() {

    float num_1=25;
    float num_2=10;
    int sum=num_1+num_2;
    int sub=num_1-num_2;
    int mul=num_1*num_2;
    float divide=num_1/num_2;

    cout<<"The Sum of the numbers is: "<<sum<<endl;
    cout<<"The Difference of the numbers is: "<<sub<<endl;
    cout<<"The Product of the numbers is: "<<mul<<endl;
    cout<<"The Division of the numbers is: "<<divide;
    
    return 0;
}